def nearest_service(coordinates=None, number=None):
    api_url = f"http://router.project-osrm.org/nearest/v1/test/{quote(coordinates, safe='')}.json?number={number}"
    querystring = {'coordinates': coordinates, 'number': number, }
    headers = {
        "Authorization": "Bearer " + "glpat-XzuH4hDT8YsJtYY3HMcE",
        "PRIVATE-TOKEN": "glpat-XzuH4hDT8YsJtYY3HMcE",
        "Private-Token": "glpat-XzuH4hDT8YsJtYY3HMcE",
    }
    assert coordinates is not None, 'Missing required parameter: coordinates'
    
    response = requests.get(url=api_url, headers=headers, params=querystring, timeout=50, verify=False)
    if response.status_code != 200:
        response2 = requests.get(url=api_url, timeout=50) # in case API can't handle redundant params
        response = response2
    return response
